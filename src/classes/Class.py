class Class:
    def __init__(self, id, name, code, building, room, time, form, studentsNum):
        self.id = id
        self.name = name
        self.code = code
        self.building = building
        self.room = room
        self.time = time
        self.form = form
        self.studentsNum = studentsNum

    def __str__(self):
        return self.code + " " + self.name
